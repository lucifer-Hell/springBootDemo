package com.example.demo.Models;

public class Intern {
    public  int eid;
    public String name;
    public String designation;
    public String team;
    public String email;

    public Intern(int eid, String name, String designation, String team, String email) {
        this.eid = eid;
        this.name = name;
        this.designation = designation;
        this.team = team;
        this.email = email;
    }

}
